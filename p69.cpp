#include<stdio.h>
#include<math.h>
int main()
{
	float a=1.6,b=1.2,c=-2.8,d=-2.3;
	printf("     CEIL FLOOR\n");
	printf("%.1f   %.f    %.f\n",a,ceil(a),floor(a));
	printf("%.1f   %.f    %.f\n",b,ceil(b),floor(b));
	printf("%.1f  %.f   %.f\n",c,ceil(c),floor(c));
	printf("%.1f  %.f   %.f\n",d,ceil(d),floor(d));
	return 0;
}
